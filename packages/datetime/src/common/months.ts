/**
 * Enumeration of calendar months.
 * Note that the enum values are numbers (with January as `0`) so they can be easily compared to `date.getMonth()`.
 */
export const enum Months {
    JANUARY,
    FEBRUARY,
    MARCH,
    APRIL,
    MAY,
    JUNE,
    JULY,
    AUGUST,
    SEPTEMBER,
    OCTOBER,
    NOVEMBER,
    DECEMBER,
}
